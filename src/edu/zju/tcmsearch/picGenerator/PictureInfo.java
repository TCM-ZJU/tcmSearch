package edu.zju.tcmsearch.picGenerator;

public class PictureInfo {

	private String bigPicLink="";
	private String smallPicLink="";
	private String originalWebsite="";
	private String picName="";
	public String getBigPicLink() {
		return bigPicLink;
	}
	public void setBigPicLink(String bigPicLink) {
		this.bigPicLink = bigPicLink;
	}
	public String getSmallPicLink() {
		return smallPicLink;
	}
	public void setSmallPicLink(String smallPicLink) {
		this.smallPicLink = smallPicLink;
	}
	public String getOriginalWebsite() {
		return originalWebsite;
	}
	public void setOriginalWebsite(String originalWebsite) {
		this.originalWebsite = originalWebsite;
	}
	public String getPicName() {
		return picName;
	}
	public void setPicName(String picName) {
		this.picName = picName;
	}	
	
}
